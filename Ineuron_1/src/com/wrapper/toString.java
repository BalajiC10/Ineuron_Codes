package com.wrapper;

public class toString {
public static void main(String[] args) {
	
		/*
		 * we can use toString() to convert any wrapper object/Primitive to
		 */
		Integer l1 = 10;
		String s1 = l1.toString();
		System.out.println(s1);
		
		Float f1 = 10.0f;
		String s2 = f1.toString();
		System.out.println(s2);
		
		Boolean b1 = true;
		String s3 = b1.toString();
		System.out.println(s3);
		
		Character ch = 'c';
		String s4 = ch.toString();
		System.out.println(s4);
		
		int b = 100;
	//	String s3 = b.toString();
		System.out.println(b);
		
		
}
}
